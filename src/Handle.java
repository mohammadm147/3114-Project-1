
public class Handle {
    
    private int key;
    private int id;
    
    public Handle()
    {
        key = 0;
        id = 0;
    }
    
    public int getKey()
    {
        return key;
    }
    
    public int getId()
    {
        return id;
    }
    
    public void setKey(int key1)
    {
        key = key1;
    }
    
    public void setId(int id1)
    {
        id = id1;
    }
}
